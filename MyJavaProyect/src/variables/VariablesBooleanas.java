package variables;

public class VariablesBooleanas {

	public static void main(String[] args) {
		boolean valor1 = true;
		boolean valor2 = false;

		System.out.println("AND: " + (valor1 && valor2));
		System.out.println("OR: " + (valor1 || valor2));
		System.out.println("NOT valor1: " + (!valor1));
		System.out.println("NOT valor2: " + (!valor2));

	}

}
